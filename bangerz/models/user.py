class User:
    def __init__(
            self,
            id,
            username,
            password,
            first_name,
            last_name,
            email,
            profile_img
            ):
        self.id = id
        self.username = username
        self.password = password
        self.first_name = first_name
        self.last_name = last_name
        self.email = email
        self.profile_img = profile_img
